public class Jefe {

   String nombre;
   String apellido;
   int edad;
   int salario;
   String dni;

   //CREAMOS LOS CONSTRUCTORES

   public Jefe(String nombre, String apellido, int edad, int salario, String dni)
   {
      this.nombre = nombre;
      this.apellido = apellido;
      this.edad = edad;
      this.salario = salario;
      this.dni = dni;
   }


}
